    private void table_update() {
        try {
            int x;

            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/inventory", "root", "");
                pst = con1.prepareStatement("select * from `toyota` ");
                ResultSet rs = pst.executeQuery();

                ResultSetMetaData rsd = rs.getMetaData();
                x = rsd.getColumnCount();

                DefaultTableModel d = (DefaultTableModel) CarTable.getModel();
                d.setRowCount(0);

                while (rs.next()) {

                    Vector v2 = new Vector();
                    for (int i = 1; i <= x; i++) {
                        v2.add(rs.getString("id"));
                        v2.add(rs.getString("make"));
                        v2.add(rs.getString("model"));
                        v2.add(rs.getString("price"));
                        v2.add(rs.getString("year"));
                        v2.add(rs.getString("fueltype"));
                        v2.add(rs.getString("color"));
                        v2.add(rs.getString("breakhorsepower"));
                        v2.add(rs.getString("drivetype"));
                        v2.add(rs.getString("quantity"));
                        v2.add(rs.getString("mileage"));
                        v2.add(rs.getString("conditionofcar"));

                    }

                    d.addRow(v2);

                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ToyotaForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ToyotaForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


    private void ProductsAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductsAddButtonActionPerformed

        String make = MakeTextBox.getText();
        String model = ModelTextBox.getText();
        double price = Double.parseDouble(PriceTextBox.getText());
        int year = Integer.parseInt(YearTextBox.getText());
        String colour = ColourTextBox.getText();
        String fuelType = FuelTypeTextBox.getText();
        int power = Integer.parseInt(PowerTextBox.getText());
        int quantity = Integer.parseInt(QuantityTextBox.getText());
        String driveType = DriveTypeComboBox.getSelectedItem().toString();
        int mileage = Integer.parseInt(MileageTextBox.getText());
        String condition = ConditionTextBox.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/inventory", "root", "");
            pst = con1.prepareStatement("insert into `toyota` (make, model, price, year, fueltype, color, breakhorsepower, "
                    + "drivetype, quantity, mileage, conditionofcar) values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
            pst.setString(1, make);
            pst.setString(2, model);
            pst.setDouble(3, price);
            pst.setInt(4, year);
            pst.setString(5, fuelType);
            pst.setString(6, colour);
            pst.setInt(7, power);
            pst.setString(8, driveType);
            pst.setInt(9, quantity);
            pst.setInt(10, mileage);
            pst.setString(11, condition);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Car Model has been added to the database!!!");
            table_update();
            //setting all values to null after insertion
            MakeTextBox.setText("");
            ModelTextBox.setText("");
            PriceTextBox.setText("");
            YearTextBox.setText("");
            ColourTextBox.setText("");
            FuelTypeTextBox.setText("");
            PowerTextBox.setText("");
            QuantityTextBox.setText("");
            MileageTextBox.setText("");
            ConditionTextBox.setText("");
            DriveTypeComboBox.setSelectedIndex(0);
            MakeTextBox.requestFocus();
        } catch (SQLException | ClassNotFoundException e) {
            e.printStackTrace();
        } finally {
            try {
                if (pst != null) {
                    pst.close();
                }
                if (con1 != null) {
                    con1.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }


    }//GEN-LAST:event_ProductsAddButtonActionPerformed

